def power(base, exponent=2):
    result = base ** exponent
    return result


result1 = power(5)
print(f"5 raised to power of 2(default exponent) is : {result1}")

result2 = power(2, 4)
print(f"2 raised to power of 4 is : {result2}")

result3 = power(10, 3)
print(f"10 raised to power of 3 is : {result3}")

